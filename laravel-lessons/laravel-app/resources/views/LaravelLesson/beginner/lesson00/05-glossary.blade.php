<section class="lesson-section lesson-section--glossary">
  <h2 class="lesson-section__title"><i data-lucide="graduation-cap"></i> 用語ミニ辞典</h2>
  <p class="lesson-section__lead">現場でよく使われる単語を簡潔にまとめました。Lesson 01 以降で再度登場します。</p>
  <div class="lesson-glossary">
    <article class="lesson-glossary__item">
      <h3 class="lesson-glossary__item-title">CLI (Command Line Interface)</h3>
      <p class="lesson-text--muted">文字で命令を入力する操作画面。ターミナルやPowerShellのこと。</p>
    </article>
    <article class="lesson-glossary__item">
      <h3 class="lesson-glossary__item-title">Package Manager</h3>
      <p class="lesson-text--muted">ソフトをまとめて管理・インストールできる仕組み。Homebrew / winget など。</p>
    </article>
    <article class="lesson-glossary__item">
      <h3 class="lesson-glossary__item-title">Repository</h3>
      <p class="lesson-text--muted">Gitで管理されるプロジェクトの保管場所。GitHub上のコード置き場。</p>
    </article>
    <article class="lesson-glossary__item">
      <h3 class="lesson-glossary__item-title">Pull Request (PR)</h3>
      <p class="lesson-text--muted">変更をチームに共有してレビューしてもらう GitHub の機能。</p>
    </article>
    <article class="lesson-glossary__item">
      <h3 class="lesson-glossary__item-title">Docker</h3>
      <p class="lesson-text--muted">アプリの動作環境をコンテナとしてまとめる仕組み。環境差異をなくせる。</p>
    </article>
  </div>
</section>
